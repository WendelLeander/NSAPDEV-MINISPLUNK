import socket
import os
import sys
import shlex

# ─────────────────────────────────────────────────────────────────────────────
# TRANSPORT LAYER
# ─────────────────────────────────────────────────────────────────────────────
BUFFER_SIZE = 4096


def _send(sock: socket.socket, message: str):
    """Send a length-prefixed UTF-8 message."""
    encoded = message.encode("utf-8")
    length  = len(encoded).to_bytes(4, "big")
    sock.sendall(length + encoded)


def _recv_all(sock: socket.socket, n: int) -> bytes:
    data = b""
    while len(data) < n:
        chunk = sock.recv(min(BUFFER_SIZE, n - len(data)))
        if not chunk:
            break
        data += chunk
    return data


def _recv_message(sock: socket.socket) -> str:
    raw_len = _recv_all(sock, 4)
    if len(raw_len) < 4:
        return ""
    msg_len = int.from_bytes(raw_len, "big")
    return _recv_all(sock, msg_len).decode("utf-8", errors="replace")


def _connect(host: str, port: int, transfer_timeout: int = None) -> socket.socket | None:
    """Open a TCP connection and return the socket, or None on failure."""
    try:
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        sock.settimeout(10)           # Timeout only for the connection handshake
        sock.connect((host, port))
        sock.settimeout(transfer_timeout)  # None = blocking (no timeout) after connected
        return sock
    except ConnectionRefusedError:
        print(f"  [System Message] ERROR: Connection refused at {host}:{port}. "
              "Is the server running?")
    except socket.timeout:
        print(f"  [System Message] ERROR: Connection timed out to {host}:{port}.")
    except Exception as e:
        print(f"  [System Message] ERROR: {e}")
    return None


def _parse_address(addr_str: str) -> tuple[str, int] | None:
    """Parse 'host:port' into (host, port). Returns None on bad format."""
    try:
        host, port_str = addr_str.rsplit(":", 1)
        return host, int(port_str)
    except Exception:
        print(f"  [System Message] ERROR: Invalid address format '{addr_str}'. "
              "Expected <IP_or_DNS>:<Port>")
        return None


# ─────────────────────────────────────────────────────────────────────────────
# COMMAND HANDLERS
# ─────────────────────────────────────────────────────────────────────────────

def cmd_ingest(args: list[str]):
    """INGEST <file_path> <IP>:<Port>"""
    if len(args) < 2:
        print("  [System Message] Usage: INGEST <file_path> <IP>:<Port>")
        return

    file_path = args[0]
    addr_str  = args[1]

    if not os.path.isfile(file_path):
        print(f"  [System Message] ERROR: File not found: '{file_path}'")
        return

    parsed = _parse_address(addr_str)
    if not parsed:
        return
    host, port = parsed

    print(f"  [System Message] Connecting to {host}:{port}…")
    sock = _connect(host, port, transfer_timeout=600)  # 5 min — enough for large files
    if not sock:
        return

    try:
        with open(file_path, "r", encoding="utf-8", errors="replace") as f:
            content = f.read()

        size = len(content.encode("utf-8"))
        print(f"  [System Message] Uploading '{os.path.basename(file_path)}' "
              f"({size / 1024:.1f} KB, {content.count(chr(10))+1} lines)…")

        _send(sock, f"UPLOAD|{size}|{content}")
        response = _recv_message(sock)
        _print_response(response)

    except Exception as e:
        print(f"  [System Message] ERROR during upload: {e}")
    finally:
        sock.close()


def cmd_query(args: list[str]):
    """QUERY <IP>:<Port> <SUB_CMD> <payload>"""
    if len(args) < 2:
        print("  [System Message] Usage: QUERY <IP>:<Port> <SUB_COMMAND> [payload]")
        return

    addr_str = args[0]
    sub_cmd  = args[1].upper()
    payload  = " ".join(args[2:]) if len(args) > 2 else ""

    parsed = _parse_address(addr_str)
    if not parsed:
        return
    host, port = parsed

    valid_cmds = {
        "SEARCH_DATE", "SEARCH_HOST", "SEARCH_DAEMON",
        "SEARCH_SEVERITY", "SEARCH_KEYWORD", "COUNT_KEYWORD",
    }
    if sub_cmd not in valid_cmds:
        print(f"  [System Message] ERROR: Unknown query sub-command '{sub_cmd}'.")
        print(f"  Valid sub-commands: {', '.join(sorted(valid_cmds))}")
        return

    if not payload and sub_cmd != "COUNT_KEYWORD":
        print(f"  [System Message] ERROR: Missing payload for {sub_cmd}.")
        return

    print(f"  [System Message] Sending query {sub_cmd} → {host}:{port}…")
    sock = _connect(host, port)
    if not sock:
        return

    try:
        _send(sock, f"QUERY|{sub_cmd}|{payload}")
        response = _recv_message(sock)
        _print_query_response(response, sub_cmd, payload)
    except Exception as e:
        print(f"  [System Message] ERROR during query: {e}")
    finally:
        sock.close()


def cmd_purge(args: list[str]):
    """PURGE <IP>:<Port>"""
    if not args:
        print("  [System Message] Usage: PURGE <IP>:<Port>")
        return

    parsed = _parse_address(args[0])
    if not parsed:
        return
    host, port = parsed

    confirm = input(f"  [System Message] WARNING: This will erase ALL indexed logs on "
                    f"{host}:{port}. Type YES to confirm: ").strip()
    if confirm.upper() != "YES":
        print("  [System Message] PURGE cancelled.")
        return

    print(f"  [System Message] Connecting to {host}:{port} to purge records…")
    sock = _connect(host, port)
    if not sock:
        return

    try:
        _send(sock, "ADMIN|PURGE|")
        response = _recv_message(sock)
        _print_response(response)
    except Exception as e:
        print(f"  [System Message] ERROR during purge: {e}")
    finally:
        sock.close()


def cmd_status(args: list[str]):
    """STATUS <IP>:<Port>"""
    if not args:
        print("  [System Message] Usage: STATUS <IP>:<Port>")
        return

    parsed = _parse_address(args[0])
    if not parsed:
        return
    host, port = parsed

    sock = _connect(host, port)
    if not sock:
        return

    try:
        _send(sock, "ADMIN|STATUS|")
        response = _recv_message(sock)
        _print_response(response)
    except Exception as e:
        print(f"  [System Message] ERROR: {e}")
    finally:
        sock.close()

def cmd_dump(args: list[str]):
    """DUMP <IP>:<Port> [limit]"""
    if not args:
        print("  [System Message] Usage: DUMP <IP>:<Port> [number_of_entries]")
        return

    parsed = _parse_address(args[0])
    if not parsed:
        return
    host, port = parsed

    limit = args[1] if len(args) > 1 else "5"

    sock = _connect(host, port)
    if not sock:
        return

    try:
        _send(sock, f"ADMIN|DUMP|{limit}")
        response = _recv_message(sock)
        parts = response.split("|", 1)
        if parts[0] == "DUMP":
            print(f"\n  [Server Response — RAM Snapshot]\n")
            print(parts[1] if len(parts) > 1 else "(empty)")
        else:
            _print_response(response)
    except Exception as e:
        print(f"  [System Message] ERROR: {e}")
    finally:
        sock.close()


# ─────────────────────────────────────────────────────────────────────────────
# RESPONSE RENDERERS
# ─────────────────────────────────────────────────────────────────────────────

def _print_response(raw: str):
    if not raw:
        print("  [Server Response] (no response received)")
        return

    parts  = raw.split("|", 2)
    status = parts[0].upper() if parts else ""

    if status == "SUCCESS":
        msg = parts[1] if len(parts) > 1 else ""
        print(f"\n  [Server Response] SUCCESS: {msg}")

    elif status == "STATUS":
        msg = "|".join(parts[1:]) if len(parts) > 1 else ""
        print(f"\n  [Server Response] {msg}")

    elif status == "ERROR":
        msg = parts[1] if len(parts) > 1 else raw
        print(f"\n  [Server Response] ERROR: {msg}")

    else:
        print(f"\n  [Server Response] {raw}")


def _print_query_response(raw: str, sub_cmd: str, payload: str):
    if not raw:
        print("  [Server Response] (no response received)")
        return

    parts  = raw.split("|", 2)
    status = parts[0].upper() if parts else ""

    if status == "RESULTS":
        n    = parts[1] if len(parts) > 1 else "?"
        body = parts[2] if len(parts) > 2 else ""

        label_map = {
            "SEARCH_DATE":     f"date '{payload}'",
            "SEARCH_HOST":     f"host '{payload}'",
            "SEARCH_DAEMON":   f"daemon '{payload}'",
            "SEARCH_SEVERITY": f"severity '{payload}'",
            "SEARCH_KEYWORD":  f"keyword '{payload}'",
        }
        label = label_map.get(sub_cmd, payload)

        print(f"\n  [Server Response] Found {n} matching entries for {label}:")
        if body.strip():
            for line in body.strip().splitlines():
                print(f"    {line}")
        else:
            print("    (no matching entries)")

    elif status == "COUNT":
        count = parts[1] if len(parts) > 1 else "?"
        print(f"\n  [Server Response] The keyword '{payload}' appears "
              f"in {count} indexed log entry/entries.")

    elif status == "ERROR":
        msg = parts[1] if len(parts) > 1 else raw
        print(f"\n  [Server Response] ERROR: {msg}")

    else:
        print(f"\n  [Server Response] {raw}")


