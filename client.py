# ─────────────────────────────────────────────────────────────────────────────
# MAIN REPL LOOP
# ─────────────────────────────────────────────────────────────────────────────

def main():
    print("=" * 62)
    print("  Mini-Splunk Syslog Analytics  —  CLI Client")
    print("=" * 62)
    print("  Type HELP for a command reference, EXIT to quit.")
    print("-" * 62)

    while True:
        try:
            raw_input = input("\nclient> ").strip()
        except (EOFError, KeyboardInterrupt):
            print("\n  [System Message] Exiting client. Goodbye.")
            sys.exit(0)

        if not raw_input:
            continue

        try:
            tokens = shlex.split(raw_input)
        except ValueError as e:
            print(f"  [System Message] Parse error: {e}")
            continue

        cmd  = tokens[0].upper()
        args = tokens[1:]

        if cmd in ("EXIT", "QUIT"):
            print("  [System Message] Exiting client. Goodbye.")
            sys.exit(0)

        elif cmd == "HELP":
            print(_HELP)

        elif cmd == "INGEST":
            cmd_ingest(args)

        elif cmd == "QUERY":
            cmd_query(args)

        elif cmd == "PURGE":
            cmd_purge(args)

        elif cmd == "STATUS":
            cmd_status(args)

        elif cmd == "DUMP":
            cmd_dump(args)

        else:
            print(f"  [System Message] Unknown command '{cmd}'. "
                  "Type HELP for a list of available commands.")


if __name__ == "__main__":
    main()
